/**
 * Pulls color variables from Figma REST API and writes design-tokens/primitives.css
 *
 * Requires FIGMA_ACCESS_TOKEN (Personal Access Token or OAuth token with file_variables:read).
 * GET /v1/files/:key/variables/local is only available on supported Figma plans — if you get 403,
 * sync tokens via Cursor Figma MCP instead, or paste values manually.
 *
 * Env (optional .env in repo root):
 *   FIGMA_ACCESS_TOKEN   — required
 *   FIGMA_FILE_KEY       — default: WRrQVjZBnyWDdtFfgwdHqp (New Design System)
 *   FIGMA_COLLECTION_NAME — default: New DS / Primitive / Color
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const OUT_FILE = join(ROOT, 'design-tokens', 'primitives.css')

function resolveModeId(coll) {
  if (coll.defaultModeId) return coll.defaultModeId
  const { modes } = coll
  if (!modes) return null
  if (Array.isArray(modes) && modes.length) return modes[0].modeId
  if (typeof modes === 'object') {
    const keys = Object.keys(modes)
    return keys[0] || null
  }
  return null
}

function loadDotEnv() {
  const p = join(ROOT, '.env')
  if (!existsSync(p)) return
  const text = readFileSync(p, 'utf8')
  for (const line of text.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq === -1) continue
    const key = trimmed.slice(0, eq).trim()
    let val = trimmed.slice(eq + 1).trim()
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1)
    }
    if (!process.env[key]) process.env[key] = val
  }
}

function figmaColorToHex(c) {
  if (!c || typeof c !== 'object' || c.type === 'VARIABLE_ALIAS') return null
  const { r, g, b, a = 1 } = c
  if (typeof r !== 'number' || typeof g !== 'number' || typeof b !== 'number') return null
  const to255 = (x) => (x <= 1 ? Math.round(x * 255) : Math.round(x))
  const R = to255(r)
  const G = to255(g)
  const B = to255(b)
  const hex6 = `#${[R, G, B].map((x) => x.toString(16).padStart(2, '0')).join('')}`
  if (typeof a === 'number' && a < 0.999) {
    const aa = Math.round(a * 255)
      .toString(16)
      .padStart(2, '0')
    return `${hex6}${aa}`
  }
  return hex6
}

function tokenToCssVarName(figmaName) {
  return `--nds-${figmaName.replace(/\//g, '-').replace(/\s+/g, '-').toLowerCase()}`
}

function buildCss({ fileKey, collectionName, rows }) {
  const fileUrl = `https://www.figma.com/design/${fileKey}/New-Design-System`
  const sorted = [...rows].sort((a, b) => a.cssName.localeCompare(b.cssName))
  const lines = sorted.map((r) => `  ${r.cssName}: ${r.hex};`)
  return `/**
 * Primitive color tokens — synced from Figma (source of truth).
 * Generated by: npm run sync:tokens
 * File: ${fileUrl}
 * Collection: ${collectionName}
 *
 * CSS names: Figma path with "/" → "-" (e.g. Neutrals/100 → --nds-neutrals-100).
 */

:root {
${lines.join('\n')}
}
`
}

async function main() {
  loadDotEnv()

  const token = process.env.FIGMA_ACCESS_TOKEN
  const fileKey = process.env.FIGMA_FILE_KEY || 'WRrQVjZBnyWDdtFfgwdHqp'
  const collectionName = process.env.FIGMA_COLLECTION_NAME || 'New DS / Primitive / Color'

  if (!token) {
    console.error(
      'Missing FIGMA_ACCESS_TOKEN. Add it to .env (see .env.example) or export it in your shell.',
    )
    process.exit(1)
  }

  const url = `https://api.figma.com/v1/files/${fileKey}/variables/local`
  const res = await fetch(url, {
    headers: { 'X-Figma-Token': token },
  })

  const body = await res.json().catch(() => ({}))

  if (res.status === 403) {
    console.error(
      'Figma returned 403. Common causes:\n' +
        '  • Variables local API requires the right plan + file_variables:read scope\n' +
        '  • Token lacks access to this file\n' +
        'Docs: https://developers.figma.com/docs/rest-api/variables-endpoints/\n',
    )
    if (body?.message) console.error('Message:', body.message)
    process.exit(1)
  }

  if (!res.ok) {
    console.error(`Figma API error ${res.status}`, body?.message || body?.err || JSON.stringify(body).slice(0, 500))
    process.exit(1)
  }

  const meta = body.meta
  if (!meta?.variables || !meta?.variableCollections) {
    console.error('Unexpected response shape (missing meta.variables).', JSON.stringify(body).slice(0, 400))
    process.exit(1)
  }

  const collections = meta.variableCollections
  const variables = meta.variables

  const collEntry = Object.values(collections).find((c) => c.name === collectionName)
  if (!collEntry) {
    const names = Object.values(collections).map((c) => c.name)
    console.error(`Collection "${collectionName}" not found. Available:`, names.join(', ') || '(none)')
    process.exit(1)
  }

  const modeId = resolveModeId(collEntry)
  if (!modeId) {
    console.error('Could not resolve a mode id for collection', collectionName)
    process.exit(1)
  }

  const rows = []
  let varIds = collEntry.variableIds
  if (!Array.isArray(varIds) || varIds.length === 0) {
    varIds = Object.values(variables)
      .filter((v) => v.variableCollectionId === collEntry.id)
      .map((v) => v.id)
  }

  for (const vid of varIds) {
    const v = variables[vid]
    if (!v || v.deletedButReferenced) continue
    if (v.resolvedType !== 'COLOR') continue
    const raw = v.valuesByMode?.[modeId]
    const hex = figmaColorToHex(raw)
    if (!hex) {
      console.warn(`Skip (alias or non-color): ${v.name}`)
      continue
    }
    rows.push({ figmaName: v.name, cssName: tokenToCssVarName(v.name), hex })
  }

  if (rows.length === 0) {
    console.error('No COLOR variables found for that collection/mode.')
    process.exit(1)
  }

  const css = buildCss({ fileKey, collectionName, rows })
  writeFileSync(OUT_FILE, css, 'utf8')
  console.log(`Wrote ${rows.length} tokens → ${OUT_FILE}`)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
